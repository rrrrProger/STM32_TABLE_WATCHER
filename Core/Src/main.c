void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	if (htim == &htim2) {
		RTC_TimeTypeDef time;
		RTC_DateTypeDef date;
		char tmp[32];
		int ret;
		double AHT10_Temp;

		if (!read_humidity_temperature) {
			HAL_I2C_Master_Transmit(&hi2c2, AHT10_Adress, &AHT10_TmpHum_Cmd, 1, 100);
		} else {
			ret = HAL_I2C_Master_Receive(&hi2c2, AHT10_Adress, (uint8_t*)AHT10_RX_Data, 6, 100);
			AHT10_ADC_Raw = (((uint32_t)AHT10_RX_Data[3] & 15) << 16) | ((uint32_t)AHT10_RX_Data[4] << 8) | AHT10_RX_Data[5];
			AHT10_Temp = (double)(AHT10_ADC_Raw * 200.00 / 1048576.00) - 50.00;
			printf("AHT10_Temp: %02f\r\n", AHT10_Temp);
			AHT10_Temperature = round(AHT10_Temp);
		}
		read_humidity_temperature = !read_humidity_temperature;

		ssd1306_SetCursor(40, 5);
		ssd1306_Fill(Black);

		HAL_RTC_GetTime(&hrtc, &time, RTC_FORMAT_BIN);
		HAL_RTC_GetDate(&hrtc, &date, RTC_FORMAT_BIN);

		snprintf(tmp, sizeof(tmp), "%02d:%02d:%02d", time.Hours, time.Minutes, time.Seconds);
		ssd1306_WriteString(tmp, Font_6x8, White);

		ssd1306_SetCursor(40, 20);

		snprintf(tmp, sizeof(tmp), "%02d.%02d.%02d", date.Date, date.Month, date.Year);
		ssd1306_WriteString(tmp, Font_6x8, White);

		if (ret == HAL_OK) {
			ssd1306_SetCursor(10, 35);
			snprintf(tmp, sizeof(tmp), "Temperature: %.02f", AHT10_Temp);
			ssd1306_WriteString(tmp, Font_6x8, White);
		}

		ssd1306_UpdateScreen();

	}
}
